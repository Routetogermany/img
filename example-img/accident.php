    </p>
  </div>

  <li>
  <em>
  Do not sign any paper unless you know and understand what you are doing.
  </em>
  </li>

  <li>
  Note the time and place of the accident and names and addresses of any witnesses.
  </li>

  <li>
  In case of an accident with private or public property, like hitting a road sign, etc, stay at the scene. Leaving such kind of accident scene can also stimulate hit and run clauses of law. Try to contact the damaged property owner. If the owner can’t be reached, leave a note behind. If possible take photographs of the scene. They are acceptable in court. Nowadays many dash cams are available in the market, better to keep one with you.
  </li>
</ul>

