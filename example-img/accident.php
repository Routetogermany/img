<h2 id="auto-recovery" class="heading-two">Calling Auto Recovery Service</h2>

<p class="para">
  It is always beneficial to sign membership of an automobile club (motoring club). The most popular automobile club in Germany is ADAC (Allgemeine Deutsche Automobil-Club). It has also partnerships in other countries. They help in minor repairs on the road and perform tow jobs in case of a vehicle breakdown. ADAC often offers discount membership packages for students. The second popular automobile club in Germany is AvD (Automobil-Club von Deutschland).
</p>

<h2 class="heading-two">
Forming an Alley for Emergency Vehicles
</h2>

<p class="para">
In emergencies on highways, an emergency alley is formed for quick access of emergency services. <br>
<em>
The emergency alley is always formed <u>next to the overtaking lane</u>. 
</em>
Please see the following two situations.
</p>

<p class="para">
<strong>
Two-lane Autobahn or Expressway
</strong>
</p>

<p class="para">
On two lane Autobahn (two lanes for each direction) or Expressway (Kraftfahrstraße) emergency alley is formed in the middle of two lanes. So if you are on the left lane you drive to the left and if you are on the right lane to the right. Please see the illustration below.
</p>

<p style="text-align:center;" class="center">        
  <img style="max-width:500px;" src="../img/example-img/2 lane Autobahn emergency alley.svg" alt="Emergency lane on 2 lane Autobahn">       
</p>


<p class="para">
<strong>
Three or higher lane Autobahn or Expressway
</strong>
</p>

<p class="para">
On Autobahn or expressway with 3 lanes for each direction, the emergency lane is formed between the left and the middle lane. 
So if you are on the left lane you drive to the left and if you are on the middle or right lane to the right. <br>
</p>

<p style="text-align:center;" class="center">        
