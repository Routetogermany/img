</p>


<h2 id="health-insurance-role" class="heading-two">Health Insurance</h2>

<p class="para">
  Health insurance is mandatory in Germany. In case you cause an accident your health insurance company pays your medical bills. If you cause an injury to another person in an accident, your vehicle insurance company will pay the medical bills of the injured person.
  Please see <a class="text-link" href="insurance">this page</a> for vehicle insurance.
</p>

                
