<h2 id="immediate-measures" class="heading-two">
Immediate Measures after an Accident
</h2>

<ul>
  <li>
  Hit and run in Germany is punishable with imprisonment for up to 3 years and a fine. Leaving the scene of an accident without reporting comes under hit and run.
  </li>
  <li>
  If you are involved in an accident, immediately switch on the hazard warning lights and drive on the hard shoulder.
  </li>
  <li>
  Immediately put on your safety vest (all helpers should wear a safety vest).
  </li>
  <li>
  Put the warning triangle on the right side of the road (at least 150m away). 
  </li>
  <li>
  Provide first aid to the injured, but do not move any critically injured person. (Even if you are not involved in an accident, but are an early arriver at the scene.)
  <br>
  First aid training is mandatory to obtain a German driver's license. Please see the page <a href="driving-license" target="new" class="text-link">Driver's license</a> for more details about the German driver's license.
  </li>
  <li>
  All persons who are not able to provide first aid  (for example, children) must stay behind the guardrail next to the hard shoulder.
  </li>
  <li>
  Call the emergency services using your mobile phone or make an emergency call from an orange-colored emergency call box (Notrufsäule).
  The emergency numbers in Germany are
  <strong>110 (Police)</strong> and
  <strong> 112 (Fire Brigade & Ambulance)</strong>. They can be dialed free of charge from any national mobile phone sim or public call box. Note that you can’t call 112 with a phone without a SIM card. Through the 112 number, you can call for an ambulance (Krankenwagen). In an emergency on a major road (motorway, highway, secondary road), look at the white
  delineator (reflecting guide post) by the side of the road for arrows pointing in the direction of the nearest emergency telephone. 
  </li>

  <div class="card-center">
    <img alt="Reflecting guide post pointing towards nearest emergency telephone."
