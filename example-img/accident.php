<h2 id="share-location" class="heading-two">
How to share your location with emergency services? 
</h2>

<p class="para">
Please note the following to state your location on the road.
</p>

<ul>
  <li>
  Name the road or Autobahn you are currently on (for example A10).
  </li>
  <li>
  Name the direction of travel. 
  </li>
  <li>
  On an Autobahn, if possible, name the two exit points between which you are currently located.
  </li>
  <li>  
  If you don't know the names of exit points, look for a <strong>kilometer number sign</strong> on the right side of the road and name the kilometer number. These signs are available on Autobahn as well as on normal roads. kilometer number sings are placed after every half a kilometer. <a target="new" class="text-link" href="https://upload.wikimedia.org/wikipedia/commons/7/78/BAB-A6-km-565.jpg">Click here</a> to see an example of a kilometer number sign on an Autobahn.
  </li>
</ul>

